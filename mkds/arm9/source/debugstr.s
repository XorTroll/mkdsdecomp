NTR_BEGIN_ASM_FN PrintInit
    mov r0, #0x4000000
	add r0, r0, #0x304	@4000304h - NDS9 - POWCNT1 - Graphics Power Control Register (R/W)
	
	ldr r1, =#0x8003		@2D + Enable
	str r1, [r0]
			
	mov r0, #0x04000000	@4000000h - DISPCNT - LCD Control (Read/Write)
	mov r1, #0x00020000
	str r1, [r0]		@2  Engine A only: VRAM Display (Bitmap from block selected in DISPCNT.18-19)
	
	mov r0, #0x04000000
	add r0, r0, #0x240
	mov r1, #0x80		@Enable
	strb r1, [r0]		@4000240h - NDS9 - VRAMCNT_A - 8bit - VRAM-A (128K) Bank Control (W)
    bx lr

NTR_BEGIN_ASM_FN PrintString
	STMFD sp!,{r0-r12, lr}
PrintStringAgain:
        ldrB r0,[r1],#1
		cmps r0,#0
		beq PrintStringDone
		bl PrintChar 
		b PrintStringAgain
PrintStringDone:
	LDMFD sp!,{r0-r12, pc}
	
